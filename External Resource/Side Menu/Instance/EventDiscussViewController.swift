import UIKit

class EventDiscussViewController: UIViewController, ENSideMenuDelegate {
    
    
    override func viewDidLoad() {
        super.viewDidLoad()
    }

    override func didReceiveMemoryWarning() {
        super.didReceiveMemoryWarning()
        // Dispose of any resources that can be recreated.
    }
    
    override func viewWillAppear(animated: Bool) {
        self.sideMenuController()?.sideMenu?.delegate = self
    }
    
    override func viewDidAppear(animated: Bool) {
        AppStatus.Event.currentlyDisplayedView = AppStatus.Event.ViewName.Discuss
        self.sideMenuController()?.sideMenu?.resetMenuSelectionForRow(AppStatus.Event.ViewName.Discuss.rawValue)
    }
    
    // MARK: - ENSideMenu Delegate
    func sideMenuWillOpen() {
        println("sideMenuWillOpen")
    }
    
    func sideMenuWillClose() {
        println("sideMenuWillClose")
    }
    
    func sideMenuShouldOpenSideMenu() -> Bool {
        println("sideMenuShouldOpenSideMenu")
        return true
    }
    
}

